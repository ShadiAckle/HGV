export function getMetricsForDomain(domain: DataDomainTag): GovernedMetric[] {
  return GOVERNED_SEMANTIC_METRICS.filter((m) => m.domain === domain);
}

export function getBindingsForTable(tableName: string): ApiUiBinding[] {
  return API_UI_BINDINGS.filter((b) => b.table.includes(tableName));
}
